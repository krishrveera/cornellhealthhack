# QUALITY GATE 3: CLIPPING DETECTION
# =============================================================================
def gate3_check_clipping(audio: np.ndarray, config: PipelineConfig,
                          report: QualityReport) -> bool:
    """
    Detect digital clipping — samples at or near the absolute digital ceiling.

    Clipping corrupts perturbation measures (jitter, shimmer) and spectral
    features. We flag if more than 0.1% of samples are within 0.1% of ±1.0.

    Note: B2AI avoids clipping via hardware (headset mic at fixed distance).
    For consumer recordings without controlled gain, this check is essential.
    """
    clipped = np.abs(audio) >= (1.0 - config.clipping_margin)
    clipping_fraction = np.mean(clipped)

    passed = clipping_fraction <= config.max_clipping_fraction
    report.gate_clipping_fraction = clipping_fraction
    report.gate_clipping_pass = passed

    status = "PASS" if passed else "FAIL"
    print(f"[Gate 3] Clipping: {clipping_fraction*100:.4f}% of samples clipped → {status}")
    print(f"         Threshold: ≤{config.max_clipping_fraction*100:.2f}%")

    if not passed:
        # Show where clipping occurs
        fig, ax = plt.subplots(1, 1, figsize=(14, 2))
        t = np.arange(len(audio)) / 16000  # approximate for display
        ax.plot(t, audio, linewidth=0.3, color='#607D8B')
        clip_indices = np.where(clipped)[0]
        if len(clip_indices) > 0:
            ax.scatter(clip_indices / 16000, audio[clip_indices],
                      color='red', s=1, zorder=5, label='Clipped samples')
        ax.axhline(y=1.0 - config.clipping_margin, color='red', linestyle='--',
                   alpha=0.5, linewidth=0.8)
        ax.axhline(y=-(1.0 - config.clipping_margin), color='red', linestyle='--',
                   alpha=0.5, linewidth=0.8)
        ax.set_title("Clipping Detection", fontweight='bold')
        ax.set_xlabel("Time (s)")
        ax.legend()
        plt.tight_layout()
        plt.show()

    return passed


