def process_b2ai_audio(filepath: str):
    """
    Preprocesses user audio to match Bridge2AI standards and extracts features.
    """
    print(f"Loading audio from {filepath}...")
    # 1. Load the raw user audio
    raw_audio = Audio(filepath=filepath)

    # 2. Resample to 16 kHz (Bridge2AI standard)
    # Senselab handles monaural (mono) conversion natively
    print("Standardizing to 16000 Hz...")
    [standardized_audio] = resample_audios([raw_audio], resample_rate=16000)

    # 3. Extract Features
    print("Extracting acoustic features...")
    # This automatically extracts standard features (MFCCs, Pitch, Jitter, Shimmer, etc.)
    features = extract_features_from_audios([standardized_audio])

    print("Pipeline complete!")
    return standardized_audio, features[0]

