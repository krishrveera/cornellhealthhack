# QUALITY GATE 4: VOICE ACTIVITY DETECTION
# =============================================================================
def gate4_check_voice_activity(audio: np.ndarray, sr: int,
                                silence_end_sample: int,
                                config: PipelineConfig,
                                report: QualityReport) -> Tuple[bool, float]:
    """
    Energy-based Voice Activity Detection to confirm the user actually spoke.

    Uses frame-level RMS energy compared against the noise floor estimated
    from the silence region. Frame parameters match B2AI spectrogram settings:
    25ms window, 10ms hop.

    Returns:
        (has_voice, voiced_duration_sec)
    """
    frame_length = int(config.vad_frame_length_ms * sr / 1000)
    hop_length = int(config.vad_hop_length_ms * sr / 1000)

    # Estimate noise floor from silence region
    noise_region = audio[:silence_end_sample]
    noise_rms = np.sqrt(np.mean(noise_region ** 2)) + 1e-10
    energy_threshold = noise_rms * config.vad_energy_multiplier

    # Compute per-frame RMS for the signal region
    signal_region = audio[silence_end_sample:]
    n_frames = max(1, (len(signal_region) - frame_length) // hop_length + 1)

    voiced_mask = np.zeros(n_frames, dtype=bool)
    for i in range(n_frames):
        start = i * hop_length
        end = start + frame_length
        if end > len(signal_region):
            break
        frame_rms = np.sqrt(np.mean(signal_region[start:end] ** 2))
        voiced_mask[i] = frame_rms > energy_threshold

    voiced_duration = np.sum(voiced_mask) * (hop_length / sr)
    has_voice = voiced_duration >= config.min_voiced_duration_sec

    report.gate_has_voice = np.any(voiced_mask)
    report.gate_voiced_duration_sec = voiced_duration
    report.gate_min_duration_pass = has_voice

    status = "PASS" if has_voice else "FAIL"
    print(f"[Gate 4] Voice activity: {voiced_duration:.2f}s voiced → {status}")
    print(f"         Minimum required: {config.min_voiced_duration_sec}s")
    print(f"         Noise floor RMS: {noise_rms:.6f} | Threshold: {energy_threshold:.6f}")

    plot_vad(signal_region, sr, voiced_mask, frame_length, hop_length)

    return has_voice, voiced_duration


