# STEP 6: TRIM SILENCE REGION (OUTPUT VOICE ONLY)
# =============================================================================
def step6_trim_silence(audio: np.ndarray, sr: int,
                        silence_end_sample: int) -> np.ndarray:
    """
    Remove the leading silence region from the final output.

    The silence was needed for SNR estimation but should not be in the
    output file that goes to feature extraction. B2AI feature extraction
    operates on voice segments, not silence.
    """
    print(f"[Step 6] Trim leading silence region")

    before = audio.copy()
    audio_trimmed = audio[silence_end_sample:]

    print(f"         Removed first {silence_end_sample/sr:.2f}s (noise estimation region)")
    print(f"         Duration: {len(before)/sr:.2f}s → {len(audio_trimmed)/sr:.2f}s")

    plot_comparison(before, audio_trimmed, sr, sr,
                    "Remove silence prefix",
                    "6: Trim Silence Region")

    return audio_trimmed


