# MAIN PIPELINE
# =============================================================================
def run_pipeline(input_path: str,
                 output_path: str = None,
                 config: PipelineConfig = None,
                 force_continue: bool = False) -> Tuple[Optional[str], QualityReport]:
    """
    Run the complete B2AI-aligned voice preprocessing pipeline.

    Args:
        input_path: Path to input audio file (m4a, caf, wav, mp3, webm, etc.)
        output_path: Path for output WAV file (auto-generated if None)
        config: Pipeline configuration (uses defaults if None)
        force_continue: If True, continue preprocessing even if quality gate fails
                       (output will be flagged in the report)

    Returns:
        (output_wav_path, quality_report)

    Pipeline:
        Quality Gate:
            1. Silence region detection (for noise floor estimation)
            2. SNR check (≥42 ideal, ≥30 acceptable, <30 reject)
            3. Clipping detection (>0.1% clipped samples → reject)
            4. Voice activity detection (≥3s voiced audio required)
            5. AGC detection (warning if automatic gain control detected)

        Preprocessing (if gate passes):
            1. Resample to 16 kHz with Butterworth anti-alias filter
            2. High-pass filter at 80 Hz (mic roll-off compensation)
            3. CMVN spectral normalization (remove mic channel coloring)
            4. LUFS normalization (constant gain, preserves all ratios)
            5. Conditional noise reduction (only if SNR 30-42 dB)
            6. Trim leading silence region
            7. Export as 16-bit PCM WAV
    """
    if config is None:
        config = PipelineConfig()

    if output_path is None:
        base = os.path.splitext(os.path.basename(input_path))[0]
        output_path = f"{base}_b2ai_processed.wav"

    print(f"╔{'═'*58}╗")
    print(f"║  B2AI-Voice Aligned Preprocessing Pipeline               ║")
    print(f"║  Input: {os.path.basename(input_path):<49}║")
    print(f"╚{'═'*58}╝\n")

    # ── Step 0: Load ──
    audio, sr, report = step0_load_audio(input_path, config)
    print()

    # ── Quality Gate ──
    gate_passed, silence_end_sample = run_quality_gate(audio, sr, config, report)

    if not gate_passed and not force_continue:
        print("Pipeline stopped. Set force_continue=True to process anyway.")
        print(report.summary())
        return None, report

    if not gate_passed and force_continue:
        print("⚠ Quality gate FAILED but force_continue=True. Proceeding with warnings.\n")

    # ── Preprocessing ──
    print(f"\n{'='*60}")
    print(f"  PREPROCESSING")
    print(f"{'='*60}\n")

    # Step 1: Resample to 16 kHz
    audio_16k, sr_16k = step1_resample(audio, sr, config)
    silence_end_resampled = int(silence_end_sample * config.target_sr / sr)
    print()

    # Step 2: High-pass filter (microphone roll-off compensation)
    audio_hp = step2_highpass_filter(audio_16k, sr_16k, config, report)
    print()

    # Step 3: CMVN (channel/microphone normalization)
    audio_cmvn = step3_cmvn_compensation(audio_hp, sr_16k, config, report)
    print()

    # Step 4: LUFS normalization (constant gain)
    audio_normalized = step4_normalize_lufs(audio_cmvn, sr_16k, config, report)
    print()

    # Step 5: Conditional noise reduction
    audio_denoised = step5_conditional_noise_reduction(
        audio_normalized, sr_16k, report.gate_snr_verdict,
        silence_end_resampled, config, report)
    print()

    # Step 6: Trim silence
    audio_trimmed = step6_trim_silence(audio_denoised, sr_16k, silence_end_resampled)
    print()

    # Step 7: Export
    output_file = step7_export_wav(audio_trimmed, sr_16k, output_path, config, report)
    print()

    # ── Final Before/After Comparison ──
    print(f"\n{'='*60}")
    print(f"  FINAL COMPARISON: Original Input vs. Processed Output")
    print(f"{'='*60}\n")

    # Resample the original to 16k just for display comparison
    # (so the playback/spectrograms are at the same rate)
    original_for_display = librosa.resample(audio, orig_sr=sr, target_sr=sr_16k)

    fig, axes = plt.subplots(2, 2, figsize=(14, 6))
    fig.suptitle("Full Pipeline: Original Input → Final Output", fontsize=14, fontweight='bold')

    # Original waveform
    t_orig = np.arange(len(original_for_display)) / sr_16k
    axes[0, 0].plot(t_orig, original_for_display, linewidth=0.3, color='#F44336')
    axes[0, 0].set_title(f"Original Input ({sr} Hz → displayed at {sr_16k} Hz)")
    axes[0, 0].set_xlabel("Time (s)")
    axes[0, 0].set_ylabel("Amplitude")
    axes[0, 0].set_ylim(-1.05, 1.05)

    # Processed waveform
    t_proc = np.arange(len(audio_trimmed)) / sr_16k
    axes[0, 1].plot(t_proc, audio_trimmed, linewidth=0.3, color='#4CAF50')
    axes[0, 1].set_title(f"Processed Output ({sr_16k} Hz, silence trimmed)")
    axes[0, 1].set_xlabel("Time (s)")
    axes[0, 1].set_ylabel("Amplitude")
    axes[0, 1].set_ylim(-1.05, 1.05)

    # Original spectrogram
    S_orig = librosa.feature.melspectrogram(y=original_for_display, sr=sr_16k, n_mels=80)
    S_orig_db = librosa.power_to_db(S_orig, ref=np.max)
    librosa.display.specshow(S_orig_db, sr=sr_16k, x_axis='time',
                             y_axis='mel', ax=axes[1, 0], cmap='magma')
    axes[1, 0].set_title("Original — Mel Spectrogram")

    # Processed spectrogram
    S_proc = librosa.feature.melspectrogram(y=audio_trimmed, sr=sr_16k, n_mels=80)
    S_proc_db = librosa.power_to_db(S_proc, ref=np.max)
    librosa.display.specshow(S_proc_db, sr=sr_16k, x_axis='time',
                             y_axis='mel', ax=axes[1, 1], cmap='magma')
    axes[1, 1].set_title("Processed — Mel Spectrogram")

    plt.tight_layout()
    plt.show()

    # Audio playback: original vs final
    print(f"🔊 Original input ({sr} Hz):")
    display(Audio(audio, rate=sr))
    print(f"🔊 Final processed output ({sr_16k} Hz):")
    display(Audio(audio_trimmed, rate=sr_16k))

    # ── Summary ──
    print()
    print(report.summary())

    return output_file, report


