# STEP 1: RESAMPLE TO 16 kHz WITH BUTTERWORTH ANTI-ALIAS
# =============================================================================
def step1_resample(audio: np.ndarray, sr: int,
                   config: PipelineConfig) -> Tuple[np.ndarray, int]:
    """
    Resample to 16 kHz with Butterworth anti-aliasing filter.

    Bridge2AI reference (PhysioNet v1.0-v3.0):
      "Raw audio was preprocessed by converting to monaural and resampling
       to 16 kHz with a Butterworth anti-aliasing filter applied."

    The Butterworth filter is applied BEFORE downsampling to prevent aliasing.
    Cutoff is set at the new Nyquist frequency (8 kHz).
    """
    print(f"[Step 1] Resample: {sr} Hz → {config.target_sr} Hz")

    before = audio.copy()
    before_sr = sr

    if sr == config.target_sr:
        print(f"         Already at target sample rate. No resampling needed.")
        return audio, sr

    if sr > config.target_sr:
        # Apply Butterworth low-pass anti-aliasing filter before downsampling
        nyquist_new = config.target_sr / 2
        nyquist_old = sr / 2
        normalized_cutoff = nyquist_new / nyquist_old

        # Clamp to valid range (0, 1) exclusive
        normalized_cutoff = min(normalized_cutoff, 0.9999)

        sos = butter(config.butter_order, normalized_cutoff, btype='low', output='sos')
        audio_filtered = sosfilt(sos, audio).astype(np.float32)

        print(f"         Applied order-{config.butter_order} Butterworth LPF, cutoff={nyquist_new:.0f} Hz")
    else:
        audio_filtered = audio
        print(f"         Upsampling from {sr} Hz (no anti-alias filter needed)")

    # Resample using librosa (high-quality polyphase resampling)
    audio_resampled = librosa.resample(audio_filtered, orig_sr=sr, target_sr=config.target_sr)

    print(f"         Samples: {len(before)} → {len(audio_resampled)}")
    print(f"         Duration preserved: {len(before)/sr:.3f}s → {len(audio_resampled)/config.target_sr:.3f}s")

    plot_comparison(before, audio_resampled, before_sr, config.target_sr,
                    f"Resample {sr}→{config.target_sr} Hz", "1: Resample to 16 kHz")

    return audio_resampled, config.target_sr


# =============================================================================
# STEP 2: HIGH-PASS FILTER (MICROPHONE ROLL-OFF COMPENSATION)
# =============================================================================
def step2_highpass_filter(audio: np.ndarray, sr: int,
                          config: PipelineConfig,
                          report: QualityReport) -> np.ndarray:
    """
    Apply a high-pass Butterworth filter to compensate for MEMS mic roll-off.

    WHY THIS IS NEEDED:
    Phone MEMS microphones (the tiny mics in iPhones, Androids, etc.) have a
    fundamentally different low-frequency response than the headset and research
    condenser microphones used by Bridge2AI. Specifically:

      - Research mics (GRAS 40AF, AKG C520): flat response down to 20-50 Hz
      - Headset mics (B2AI recommended): flat down to ~50-80 Hz
      - Phone MEMS mics: roll off below 100-200 Hz at ~6 dB/octave

    This roll-off varies by device:
      - iPhone MEMS: typically -3dB at ~80-100 Hz
      - Samsung Galaxy MEMS: varies, often -3dB at ~100-150 Hz
      - Budget Android: can roll off as high as 200 Hz

    The Bridge2AI validation study (Awan et al., 2024) found a "strong device
    effect on L/H Ratio" directly attributable to these frequency response
    differences between microphones.

    WHAT THIS FILTER DOES:
    Rather than trying to boost the low frequencies (which would also amplify
    noise), we standardize ALL recordings to the same low-frequency behavior
    by applying a consistent high-pass filter. This removes the variable
    low-frequency content that differs between devices, creating a level
    playing field.

    The cutoff (default 80 Hz) is set at the approximate -3dB point of
    B2AI's recommended headset microphones. Content below 80 Hz is:
      - Not needed for F0 (adult speech F0 range: ~85-300 Hz)
      - Dominated by room rumble, HVAC, handling noise
      - The primary source of device-to-device variability

    After this filter, a recording from an iPhone and a Galaxy will have
    much more similar spectral tilt characteristics.
    """
    print(f"[Step 2] High-pass filter: {config.highpass_cutoff_hz} Hz "
          f"(microphone roll-off compensation)")

    before = audio.copy()

    # Design Butterworth high-pass filter
    nyquist = sr / 2
    normalized_cutoff = config.highpass_cutoff_hz / nyquist

    if normalized_cutoff >= 1.0:
        print(f"         WARNING: Cutoff {config.highpass_cutoff_hz} Hz >= Nyquist {nyquist} Hz. Skipping.")
        return audio

    sos = butter(config.highpass_order, normalized_cutoff, btype='high', output='sos')
    audio_filtered = sosfilt(sos, audio).astype(np.float32)

    report.highpass_applied = True
    report.device_id = config.device_id

    print(f"         Device: {config.device_id}")
    print(f"         Cutoff: {config.highpass_cutoff_hz} Hz, order {config.highpass_order}")
    print(f"         Purpose: Remove variable low-freq content that differs between")
    print(f"         phone mics, standardizing to B2AI headset mic behavior (~80 Hz)")

    # Show frequency response comparison
    fig, axes = plt.subplots(1, 3, figsize=(16, 3.5))

    # Before/after waveform (zoomed to see the difference)
    show_samples = min(len(before), int(0.1 * sr))  # show 100ms
    t = np.arange(show_samples) / sr * 1000  # ms
    axes[0].plot(t, before[:show_samples], linewidth=0.5, color='#2196F3',
                 alpha=0.7, label='Before')
    axes[0].plot(t, audio_filtered[:show_samples], linewidth=0.5, color='#4CAF50',
                 alpha=0.7, label='After')
    axes[0].set_title("Waveform (first 100ms)", fontweight='bold')
    axes[0].set_xlabel("Time (ms)")
    axes[0].legend()

    # Spectrum comparison
    n_fft = 2048
    S_before = np.abs(np.fft.rfft(before[:n_fft]))
    S_after = np.abs(np.fft.rfft(audio_filtered[:n_fft]))
    freqs = np.fft.rfftfreq(n_fft, 1/sr)

    axes[1].semilogy(freqs, S_before + 1e-10, linewidth=0.8, color='#2196F3',
                     alpha=0.7, label='Before')
    axes[1].semilogy(freqs, S_after + 1e-10, linewidth=0.8, color='#4CAF50',
                     alpha=0.7, label='After')
    axes[1].axvline(x=config.highpass_cutoff_hz, color='red', linestyle='--',
                    linewidth=1, label=f'Cutoff: {config.highpass_cutoff_hz} Hz')
    axes[1].set_title("Frequency Spectrum", fontweight='bold')
    axes[1].set_xlabel("Frequency (Hz)")
    axes[1].set_ylabel("Magnitude")
    axes[1].set_xlim(0, 2000)
    axes[1].legend()

    # Filter frequency response
    from scipy.signal import sosfreqz
    w, h = sosfreqz(sos, worN=2048, fs=sr)
    axes[2].plot(w, 20 * np.log10(np.abs(h) + 1e-10), linewidth=1.5, color='#FF5722')
    axes[2].axvline(x=config.highpass_cutoff_hz, color='red', linestyle='--',
                    linewidth=1, label=f'Cutoff: {config.highpass_cutoff_hz} Hz')
    axes[2].axhline(y=-3, color='gray', linestyle=':', linewidth=0.8, label='-3 dB')
    axes[2].set_title("High-pass Filter Response", fontweight='bold')
    axes[2].set_xlabel("Frequency (Hz)")
    axes[2].set_ylabel("Gain (dB)")
    axes[2].set_xlim(0, 2000)
    axes[2].set_ylim(-40, 5)
    axes[2].legend()

    plt.tight_layout()
    plt.show()

    # Audio playback: before and after
    print(f"    🔊 Before high-pass:")
    display(Audio(before, rate=sr))
    print(f"    🔊 After high-pass:")
    display(Audio(audio_filtered, rate=sr))

    return audio_filtered
def step3_cmvn_compensation(audio: np.ndarray, sr: int,
                             config: PipelineConfig,
                             report: QualityReport) -> np.ndarray:
    """
    Apply Cepstral Mean and Variance Normalization to remove channel effects.

    WHY THIS IS NEEDED:
    Even after the high-pass filter, different microphones impose different
    spectral "coloring" on the signal — a slight emphasis here, a dip there.
    This coloring is roughly constant across the recording (it's a property
    of the microphone hardware, not the speaker's voice). CMVN removes it.

    HOW IT WORKS:
    CMVN operates in the cepstral domain (on MFCCs or log-mel features):
      1. Compute the mean cepstral vector across all frames in the utterance
      2. Subtract the mean from each frame (CMN — removes channel bias)
      3. Optionally normalize variance to unit (CVN — removes channel scaling)

    This is the standard technique in speech recognition (used in Kaldi, ESPnet,
    etc.) for making models device-independent. It works because:
      - The microphone's frequency response adds a constant offset in the
        log-spectral / cepstral domain
      - Subtracting the mean removes this constant offset
      - The speaker's voice characteristics (F0, formant *ratios*, perturbation)
        are preserved because they vary frame-to-frame

    IMPORTANT: CMVN is applied to the FEATURES, not the raw audio waveform.
    However, we implement an equivalent time-domain operation here: spectral
    flattening via mean log-spectrum subtraction, which achieves the same
    effect while keeping the output as a waveform for downstream compatibility.

    For your feature extraction pipeline: if your feature extractor already
    applies CMVN (many do by default), you can skip this step to avoid
    double-normalization. Set config.apply_cmvn = False.
    """
    print(f"[Step 3] CMVN — Channel/Microphone Normalization")

    if not config.apply_cmvn:
        print(f"         Skipped (config.apply_cmvn = False)")
        print(f"         Make sure your feature extractor applies CMVN internally.")
        report.cmvn_applied = False
        return audio

    before = audio.copy()

    # Compute STFT
    n_fft = 512
    hop_length = int(config.vad_hop_length_ms * sr / 1000)  # 10ms hop
    S = librosa.stft(audio, n_fft=n_fft, hop_length=hop_length)
    magnitude = np.abs(S)
    phase = np.angle(S)

    # Compute mean log-magnitude spectrum (channel estimate)
    log_mag = np.log(magnitude + 1e-10)
    mean_log_spectrum = np.mean(log_mag, axis=1, keepdims=True)

    # Subtract mean log-spectrum (removes constant channel coloration)
    log_mag_normalized = log_mag - mean_log_spectrum

    # Optional: variance normalization
    std_log_spectrum = np.std(log_mag, axis=1, keepdims=True) + 1e-10
    log_mag_normalized = log_mag_normalized / std_log_spectrum

    # Reconstruct
    magnitude_normalized = np.exp(log_mag_normalized)
    S_normalized = magnitude_normalized * np.exp(1j * phase)
    audio_cmvn = librosa.istft(S_normalized, hop_length=hop_length, length=len(audio))
    audio_cmvn = audio_cmvn.astype(np.float32)

    # Preserve original peak level
    peak_before = np.max(np.abs(before))
    peak_after = np.max(np.abs(audio_cmvn))
    if peak_after > 0:
        audio_cmvn = audio_cmvn * (peak_before / peak_after)

    report.cmvn_applied = True

    print(f"         Applied spectral mean and variance normalization")
    print(f"         This removes microphone-specific frequency coloring")
    print(f"         so an iPhone recording and Galaxy recording produce")
    print(f"         more similar spectral features.")

    # Visualization: before/after mean spectrum
    fig, axes = plt.subplots(1, 2, figsize=(14, 3.5))

    S_before_mel = librosa.feature.melspectrogram(y=before, sr=sr, n_mels=80)
    S_after_mel = librosa.feature.melspectrogram(y=audio_cmvn, sr=sr, n_mels=80)

    mean_mel_before = np.mean(librosa.power_to_db(S_before_mel), axis=1)
    mean_mel_after = np.mean(librosa.power_to_db(S_after_mel), axis=1)

    mel_freqs = librosa.mel_frequencies(n_mels=80, fmin=0, fmax=sr/2)

    axes[0].plot(mel_freqs, mean_mel_before, linewidth=1.5, color='#2196F3',
                 label='Before CMVN')
    axes[0].plot(mel_freqs, mean_mel_after, linewidth=1.5, color='#4CAF50',
                 label='After CMVN')
    axes[0].set_title("Mean Mel Spectrum (channel shape removed)", fontweight='bold')
    axes[0].set_xlabel("Frequency (Hz)")
    axes[0].set_ylabel("Power (dB)")
    axes[0].legend()

    # Show the estimated channel response that was removed
    channel_estimate_db = 20 * np.log10(np.exp(mean_log_spectrum.flatten()) + 1e-10)
    freqs_fft = np.linspace(0, sr/2, len(channel_estimate_db))
    axes[1].plot(freqs_fft, channel_estimate_db, linewidth=1.5, color='#FF5722')
    axes[1].set_title("Estimated Channel Response (removed)", fontweight='bold')
    axes[1].set_xlabel("Frequency (Hz)")
    axes[1].set_ylabel("Magnitude (dB)")
    axes[1].axhline(y=0, color='gray', linestyle='--', linewidth=0.8)

    plt.tight_layout()
    plt.show()

    # Audio playback: before and after
    print(f"    🔊 Before CMVN:")
    display(Audio(before, rate=sr))
    print(f"    🔊 After CMVN:")
    display(Audio(audio_cmvn, rate=sr))

    return audio_cmvn
def step4_normalize_lufs(audio: np.ndarray, sr: int,
                          config: PipelineConfig,
                          report: QualityReport) -> np.ndarray:
    """
    Normalize loudness to a consistent LUFS target using EBU R128.

    This applies a CONSTANT GAIN across the entire signal, which:
      ✓ Preserves SNR (signal and noise scaled equally)
      ✓ Preserves relative amplitude dynamics
      ✓ Preserves shimmer, jitter, HNR ratios
      ✗ Does NOT compress dynamic range (unlike AGC)

    This is mathematically equivalent to the user being at a slightly
    different distance from the microphone — a linear operation.

    Target: -23 LUFS (EBU R128 broadcast standard)
    """
    print(f"[Step 4] LUFS Normalization → target: {config.target_lufs} LUFS")

    before = audio.copy()

    meter = pyln.Meter(sr)
    loudness_before = meter.integrated_loudness(audio)
    report.lufs_before = loudness_before

    print(f"         Before: {loudness_before:.1f} LUFS")

    # Apply constant gain normalization
    audio_normalized = pyln.normalize.loudness(audio, loudness_before, config.target_lufs)

    # Prevent any clipping from the gain increase
    peak = np.max(np.abs(audio_normalized))
    if peak > 0.99:
        audio_normalized = audio_normalized * (0.99 / peak)
        print(f"         Peak limiter applied (peak was {peak:.4f})")

    loudness_after = meter.integrated_loudness(audio_normalized)
    report.lufs_after = loudness_after
    gain_applied = loudness_after - loudness_before

    print(f"         After: {loudness_after:.1f} LUFS (gain: {gain_applied:+.1f} dB)")
    print(f"         This is a constant gain — SNR and amplitude ratios preserved.")

    plot_comparison(before, audio_normalized, sr, sr,
                    f"LUFS: {loudness_before:.1f} → {loudness_after:.1f}",
                    "4: LUFS Normalization (constant gain)")

    return audio_normalized


