def visualize_bridge2ai_features(features_dict, title_prefix=""):
    """
    Specifically tailored to visualize Bridge2AI-Voice extracted features in Colab.
    """
    for key, value in features_dict.items():
        name = key.lower()
        current_title = f"{title_prefix} > {key}" if title_prefix else key

        # 1. Handle Nested Dictionaries recursively
        if isinstance(value, dict):
            visualize_bridge2ai_features(value, current_title)
            continue

        # 2. Handle Text (Transcriptions)
        if isinstance(value, str):
            print(f"\n📝 [{current_title}] Transcription:")
            print(f"\"{value}\"")
            continue

        # 3. Handle Tabular Data (OpenSMILE)
        if isinstance(value, pd.DataFrame):
            print(f"\n📊 [{current_title}] OpenSMILE / Tabular Data (Shape: {value.shape}):")
            display(value.head())
            continue

        # Standardize arrays/tensors for plotting
        if isinstance(value, torch.Tensor):
            val_np = value.squeeze().detach().cpu().numpy()
        elif isinstance(value, np.ndarray):
            val_np = np.squeeze(value)
        else:
            # If it is a standard scalar (e.g., a single jitter/shimmer value)
            print(f"\n🔢 [{current_title}]: {value}")
            continue

        # 4. Handle 2D Heatmaps (Spectrograms, MFCCs, PPGs)
        if val_np.ndim == 2:
            plt.figure(figsize=(12, 4))

            # Select colormap based on feature type
            if "mfcc" in name:
                cmap = 'coolwarm'
                y_label = "MFCC Coefficients"
