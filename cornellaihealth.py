@dataclass
class PipelineConfig:
    """All configurable thresholds in one place."""

    # Target sample rate: Bridge2AI preprocesses all audio to 16 kHz
    # Reference: PhysioNet B2AI-Voice v1.0-v3.0 documentation
    target_sr: int = 16000

    # Bit depth for output WAV
    bit_depth: int = 16

    # SNR thresholds (dB)
    # Reference: Deliyski et al. (2005) via ASHA Protocol (Patel et al., 2018)
    #   >= 42 dB: ideal (perturbation error < 1%)
    #   >= 30 dB: acceptable
    #   <  30 dB: reject
    snr_ideal: float = 42.0
    snr_acceptable: float = 30.0
    snr_reject: float = 30.0

    # Silence period: seconds of silence expected BEFORE the voice task
    # Reference: ASHA Protocol recommends ~5s of quiet for noise floor estimation
    silence_duration_sec: float = 3.0

    # Minimum voiced duration (seconds) after trimming
    # AVQI protocol extracts middle 3s from a 5s sustained vowel
    min_voiced_duration_sec: float = 3.0

    # Clipping: max fraction of samples at digital ceiling before rejection
    # Samples within this margin of ±1.0 are considered clipped
    clipping_margin: float = 0.001  # within 0.1% of full scale
    max_clipping_fraction: float = 0.001  # reject if > 0.1% of samples clipped

    # LUFS normalization target
    # EBU R128 broadcast standard; constant gain preserves SNR & amplitude ratios
    target_lufs: float = -23.0

    # Noise reduction: only applied in the 30-42 dB SNR "marginal" band
    # Stationary noise reduction strength (0.0 - 1.0)
    noise_reduce_strength: float = 0.7

    # Butterworth anti-aliasing filter order
    # Reference: B2AI uses Butterworth anti-aliasing before resampling
    butter_order: int = 8

    # Voice Activity Detection: energy-based threshold
    # Frame is "voiced" if its RMS energy > (noise_floor_rms * this multiplier)
    vad_energy_multiplier: float = 3.0
    vad_frame_length_ms: float = 25.0  # match B2AI spectrogram window
    vad_hop_length_ms: float = 10.0    # match B2AI spectrogram hop

    # ── Device / Microphone Compensation ──
    # Device identifier string passed from the recording app
    # e.g. "iPhone15,2" (iPhone 15 Pro), "SM-S928U" (Galaxy S25 Ultra)
    # Used to look up frequency response correction curves
    device_id: str = "unknown"

    # Whether to apply CMVN (Cepstral Mean and Variance Normalization)
    # Removes channel/microphone coloration from MFCC-based features
    # Standard technique in speech recognition for device independence
    apply_cmvn: bool = True

    # High-pass filter cutoff (Hz) to remove low-frequency mic roll-off
    # Phone MEMS mics typically roll off below 100-200 Hz at 6 dB/octave
    # B2AI headset mics are flat down to ~50 Hz
    # This filter standardizes the low-frequency behavior across devices
    highpass_cutoff_hz: float = 80.0
    highpass_order: int = 4

    # AGC detection sensitivity
    # If the standard deviation of frame-level RMS is abnormally low relative
    # to the mean, AGC is likely active (it compresses dynamic range)
    agc_coefficient_of_variation_threshold: float = 0.15


