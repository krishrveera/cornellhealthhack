def step0_load_audio(input_path: str, config: PipelineConfig) -> Tuple[np.ndarray, int, QualityReport]:
    """
    Load any audio format and convert to mono float32 numpy array.

    iOS recording formats:
      - .m4a (AAC) — most common from AVAudioRecorder
      - .caf (Core Audio Format) — from AVAudioEngine with LPCM
      - .wav — possible but not native iOS default

    This step uses librosa which handles m4a, caf, mp3, webm, ogg, flac, wav
    via ffmpeg backend.

    Bridge2AI reference:
      Raw audio was preprocessed by converting to monaural.
    """
    report = QualityReport()
    report.input_file = os.path.basename(input_path)
    report.input_format = os.path.splitext(input_path)[1].lower()

    # Load at native sample rate, mono
    audio, sr = librosa.load(input_path, sr=None, mono=True)

    report.input_sr = sr
    report.input_duration_sec = len(audio) / sr
    report.input_channels = 1  # forced mono by librosa

    print(f"[Step 0] Loaded: {report.input_file}")
    print(f"         Format: {report.input_format} | {sr} Hz | {report.input_duration_sec:.2f}s")
    print(f"         Converted to: mono float32 numpy array")

    return audio, sr, report


# =============================================================================
# QUALITY GATE 1: SILENCE REGION DETECTION
# =============================================================================
def gate1_check_silence_region(audio: np.ndarray, sr: int,
                                config: PipelineConfig) -> Tuple[bool, int]:
    """
    Verify that the recording begins with a silence period for noise estimation.

    The ASHA Protocol (Patel et al., 2018) recommends recording ~5 seconds of
    quiet before phonation to establish the noise floor. We check for at least
    config.silence_duration_sec seconds of low-energy audio at the start.

    Returns:
        (has_silence_region, silence_end_sample)
    """
    silence_samples = int(config.silence_duration_sec * sr)

    if len(audio) < silence_samples:
        print(f"[Gate 1] FAIL: Recording too short for silence region analysis")
        return False, 0

    # Check that the first N seconds are significantly quieter than the rest
    silence_region = audio[:silence_samples]
    signal_region = audio[silence_samples:]

    if len(signal_region) == 0:
        print(f"[Gate 1] FAIL: No audio after silence region")
        return False, 0

    silence_rms = np.sqrt(np.mean(silence_region ** 2)) + 1e-10
    signal_rms = np.sqrt(np.mean(signal_region ** 2)) + 1e-10

    ratio_db = 20 * np.log10(signal_rms / silence_rms)

    # The signal region should be noticeably louder than the silence region
    has_silence = ratio_db > 10  # at least 10 dB difference

    status = "PASS" if has_silence else "WARN"
    print(f"[Gate 1] Silence region check: {status}")
    print(f"         Silence RMS: {silence_rms:.6f} | Signal RMS: {signal_rms:.6f}")
    print(f"         Signal-to-silence ratio: {ratio_db:.1f} dB")

    if not has_silence:
        print(f"         WARNING: First {config.silence_duration_sec}s not clearly silent.")
        print(f"         SNR estimation may be unreliable.")

    return has_silence, silence_samples


# =============================================================================
# QUALITY GATE 2: SNR CHECK
# =============================================================================
def gate2_check_snr(audio: np.ndarray, sr: int, silence_end_sample: int,
                    config: PipelineConfig, report: QualityReport) -> Tuple[str, float]:
    """
    Compute Signal-to-Noise Ratio using the silence region as noise estimate.

    Thresholds (Deliyski et al., 2005 / ASHA Protocol):
      >= 42 dB: ideal — perturbation measurement error < 1%
      >= 30 dB: acceptable
      <  30 dB: reject

    Returns:
        (verdict, snr_db) where verdict is "ideal", "acceptable", "marginal", or "reject"
    """
    noise_region = audio[:silence_end_sample]
    signal_region = audio[silence_end_sample:]

    noise_power = np.mean(noise_region ** 2) + 1e-20
    signal_power = np.mean(signal_region ** 2) + 1e-20

    snr_db = 10 * np.log10(signal_power / noise_power)

    if snr_db >= config.snr_ideal:
        verdict = "ideal"
    elif snr_db >= config.snr_acceptable:
        verdict = "acceptable"
    else:
        verdict = "reject"

    report.gate_snr_db = snr_db
    report.gate_snr_verdict = verdict

    color_map = {"ideal": "🟢", "acceptable": "🟡", "reject": "🔴"}
    print(f"[Gate 2] SNR: {snr_db:.1f} dB → {color_map[verdict]} {verdict.upper()}")
    print(f"         Thresholds: ≥42 dB ideal | ≥30 dB acceptable | <30 dB reject")

    plot_snr_analysis(audio, sr, silence_end_sample, snr_db, config)

    return verdict, snr_db


