# STEP 5: CONDITIONAL NOISE REDUCTION (MARGINAL SNR ONLY)
# =============================================================================
def step5_conditional_noise_reduction(audio: np.ndarray, sr: int,
                                       snr_verdict: str,
                                       silence_end_sample_resampled: int,
                                       config: PipelineConfig,
                                       report: QualityReport) -> np.ndarray:
    """
    Apply minimal spectral gating noise reduction ONLY if SNR is marginal (30-42 dB).

    Bridge2AI philosophy: prevent noise at source (headset mic), don't clean algorithmically.
    But for consumer apps without guaranteed headsets, we need a pragmatic fallback:
      >= 42 dB: don't touch it
      30-42 dB: light spectral gating using noise profile from silence region, flag as processed
      <  30 dB: already rejected by quality gate

    Uses stationary noise reduction with the noise profile estimated from
    the silence period captured at the start of the recording.
    """
    print(f"[Step 5] Conditional Noise Reduction (SNR verdict: {snr_verdict})")

    if snr_verdict == "ideal":
        print(f"         SNR is ideal (≥42 dB). No noise reduction applied.")
        print(f"         (Matches B2AI approach: clean recordings need no processing)")
        report.noise_reduction_applied = False
        return audio

    # snr_verdict is "acceptable" — apply light noise reduction
    print(f"         SNR is acceptable but not ideal. Applying light spectral gating...")

    before = audio.copy()

    # Use silence region as noise profile
    # Adjust silence_end_sample for the resampled audio
    noise_clip = audio[:silence_end_sample_resampled]

    if len(noise_clip) < sr // 4:  # need at least 0.25s for good noise estimate
        print(f"         WARNING: Noise clip too short ({len(noise_clip)/sr:.2f}s). Skipping.")
        report.noise_reduction_applied = False
        return audio

    audio_cleaned = nr.reduce_noise(
        y=audio,
        sr=sr,
        y_noise=noise_clip,
        prop_decrease=config.noise_reduce_strength,
        stationary=True,  # assume stationary noise (HVAC, hum, etc.)
        n_fft=512,
        hop_length=int(config.vad_hop_length_ms * sr / 1000),
    )

    report.noise_reduction_applied = True
    print(f"         Noise reduction applied (strength: {config.noise_reduce_strength})")
    print(f"         ⚠ Recording flagged as algorithmically processed")

    plot_comparison(before, audio_cleaned, sr, sr,
                    "Spectral gating noise reduction",
                    "5: Conditional Noise Reduction")

    return audio_cleaned


