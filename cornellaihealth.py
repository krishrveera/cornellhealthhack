# QUALITY GATE 5: AGC (AUTOMATIC GAIN CONTROL) DETECTION
# =============================================================================
def gate5_check_agc(audio: np.ndarray, sr: int,
                    silence_end_sample: int,
                    config: PipelineConfig,
                    report: QualityReport) -> bool:
    """
    Detect whether Automatic Gain Control (AGC) is active on the recording.

    WHY THIS MATTERS:
    iOS and Android audio stacks can apply AGC at the hardware/driver level
    before your app sees the samples. AGC dynamically adjusts gain frame-by-frame,
    which directly corrupts amplitude-dependent voice biomarkers:
      - Shimmer (cycle-to-cycle amplitude variation) is distorted
      - HNR (harmonics-to-noise ratio) is altered
      - Intensity contours are compressed
      - L/H spectral ratio can shift

    Bridge2AI avoids this by using a headset microphone at fixed distance in a
    controlled environment — AGC has little to do when the input level is stable.

    DETECTION METHOD:
    In natural speech, voiced segments have substantial amplitude variation
    (prosodic stress, loudness changes, etc.). AGC compresses this variation,
    producing unnaturally uniform frame-level energy. We detect this by computing
    the coefficient of variation (std/mean) of frame-level RMS energy across
    voiced segments. If CV is abnormally low, AGC is likely active.

    This is a WARNING, not a hard rejection — the recording can still be used,
    but amplitude-dependent features should be interpreted with caution.

    Returns:
        True if AGC is likely NOT active (good), False if AGC detected (warning)
    """
    print(f"[Gate 5] AGC (Automatic Gain Control) Detection")

    signal_region = audio[silence_end_sample:]
    if len(signal_region) < sr:
        print(f"         Signal too short for AGC analysis. Skipping.")
        return True

    # Compute frame-level RMS energy
    frame_length = int(config.vad_frame_length_ms * sr / 1000)
    hop_length = int(config.vad_hop_length_ms * sr / 1000)
    n_frames = (len(signal_region) - frame_length) // hop_length + 1

    frame_rms = np.zeros(n_frames)
    for i in range(n_frames):
        start = i * hop_length
        end = start + frame_length
        frame_rms[i] = np.sqrt(np.mean(signal_region[start:end] ** 2))

    # Only analyze frames with actual voice (above noise floor)
    noise_rms = np.sqrt(np.mean(audio[:silence_end_sample] ** 2)) + 1e-10
    voiced_rms = frame_rms[frame_rms > noise_rms * config.vad_energy_multiplier]

    if len(voiced_rms) < 10:
        print(f"         Not enough voiced frames for AGC analysis. Skipping.")
        return True

    # Coefficient of variation: std / mean
    cv = np.std(voiced_rms) / (np.mean(voiced_rms) + 1e-10)

    agc_likely = cv < config.agc_coefficient_of_variation_threshold
    report.agc_detected = agc_likely

    if agc_likely:
        report.agc_warning = (
            f"CV={cv:.3f} < {config.agc_coefficient_of_variation_threshold} — "
            f"amplitude variation is abnormally low, suggesting AGC is active. "
            f"Shimmer, HNR, and intensity features may be unreliable."
        )
        print(f"         ⚠ WARNING: AGC likely active (CV={cv:.3f})")
        print(f"         Voiced RMS variation is abnormally uniform.")
        print(f"         Amplitude-dependent features (shimmer, HNR) may be distorted.")
        print(f"         Recommendation: disable AGC in your iOS/Android recording config.")
        print(f"         iOS: set AVAudioSession mode to .measurement")
        print(f"         Android: use VOICE_RECOGNITION source or disable NOISE_SUPPRESSOR")
    else:
        print(f"         PASS: No AGC detected (CV={cv:.3f})")
        print(f"         Amplitude variation is within normal range.")

    # Visualization
    fig, axes = plt.subplots(1, 2, figsize=(14, 3))

    # Frame-level RMS over time
    t_frames = np.arange(n_frames) * hop_length / sr
    axes[0].plot(t_frames, frame_rms, linewidth=0.5, color='#2196F3', alpha=0.7)
    axes[0].axhline(y=noise_rms * config.vad_energy_multiplier,
                    color='red', linestyle='--', linewidth=0.8, label='Voice threshold')
    axes[0].set_title("Frame-level RMS Energy", fontweight='bold')
    axes[0].set_xlabel("Time (s)")
    axes[0].set_ylabel("RMS")
    axes[0].legend()

    # Histogram of voiced frame energies
    color = '#F44336' if agc_likely else '#4CAF50'
    axes[1].hist(voiced_rms, bins=30, color=color, alpha=0.7, edgecolor='white')
    axes[1].axvline(x=np.mean(voiced_rms), color='black', linestyle='--',
                    linewidth=1, label=f'Mean={np.mean(voiced_rms):.4f}')
    axes[1].set_title(f"Voiced Frame RMS Distribution (CV={cv:.3f})", fontweight='bold')
    axes[1].set_xlabel("RMS Energy")
    axes[1].set_ylabel("Count")
    axes[1].legend()

    plt.tight_layout()
    plt.show()

    return not agc_likely


