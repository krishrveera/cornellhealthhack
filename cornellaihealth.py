# STEP 7: EXPORT TO WAV
# =============================================================================
def step7_export_wav(audio: np.ndarray, sr: int, output_path: str,
                     config: PipelineConfig, report: QualityReport) -> str:
    """
    Export the processed audio as a 16-bit PCM WAV file.

    Bridge2AI reference:
      - Audio resampled to 16 kHz mono
      - Feature extraction via OpenSMILE, Praat/Parselmouth, torchaudio, Whisper
      - All operate on standard WAV input

    Output format: 16-bit PCM WAV, mono, 16 kHz
    """
    print(f"[Step 7] Export to WAV: {output_path}")

    # Scale float32 [-1, 1] to int16 range
    audio_int16 = np.clip(audio * 32767, -32768, 32767).astype(np.int16)

    wavfile.write(output_path, sr, audio_int16)

    report.output_sr = sr
    report.output_duration_sec = len(audio) / sr

    file_size = os.path.getsize(output_path)
    print(f"         Format: PCM WAV, {config.bit_depth}-bit, {sr} Hz, mono")
    print(f"         Duration: {report.output_duration_sec:.2f}s")
    print(f"         File size: {file_size / 1024:.1f} KB")

    return output_path


