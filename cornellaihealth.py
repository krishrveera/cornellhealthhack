# QUALITY GATE: AGGREGATE DECISION
# =============================================================================
def run_quality_gate(audio: np.ndarray, sr: int, config: PipelineConfig,
                     report: QualityReport) -> Tuple[bool, int]:
    """
    Run all quality gates and return aggregate pass/fail.

    Returns:
        (passed, silence_end_sample)
    """
    print(f"\n{'='*60}")
    print(f"  QUALITY GATE")
    print(f"{'='*60}\n")

    failures = []

    # Gate 1: Silence region
    has_silence, silence_end_sample = gate1_check_silence_region(audio, sr, config)
    report.gate_silence_detected = has_silence
    if not has_silence:
        # Use a default split if no clear silence region
        silence_end_sample = int(config.silence_duration_sec * sr)
        if silence_end_sample >= len(audio):
            silence_end_sample = len(audio) // 4
        print(f"         Using default silence region: first {silence_end_sample/sr:.1f}s")
    print()

    # Gate 2: SNR
    snr_verdict, snr_db = gate2_check_snr(audio, sr, silence_end_sample, config, report)
    if snr_verdict == "reject":
        failures.append(f"SNR too low ({snr_db:.1f} dB < {config.snr_reject} dB)")
    print()

    # Gate 3: Clipping
    clipping_ok = gate3_check_clipping(audio, config, report)
    if not clipping_ok:
        failures.append(f"Clipping detected ({report.gate_clipping_fraction*100:.3f}%)")
    print()

    # Gate 4: Voice activity
    has_voice, voiced_dur = gate4_check_voice_activity(
        audio, sr, silence_end_sample, config, report)
    if not has_voice:
        failures.append(f"Insufficient voiced audio ({voiced_dur:.2f}s < {config.min_voiced_duration_sec}s)")
    print()

    # Gate 5: AGC detection (warning only, does not cause rejection)
    agc_ok = gate5_check_agc(audio, sr, silence_end_sample, config, report)
    if not agc_ok:
        print(f"         (AGC is a warning, not a hard rejection)")
    print()

    passed = len(failures) == 0
    report.gate_passed = passed
    report.gate_failure_reasons = failures

    status = "✅ PASSED" if passed else "❌ FAILED"
    print(f"{'='*60}")
    print(f"  Quality Gate Result: {status}")
    if failures:
        for f in failures:
            print(f"    ✗ {f}")
    print(f"{'='*60}\n")

    return passed, silence_end_sample


